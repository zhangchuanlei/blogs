package com.blogs.test;

import java.util.ArrayList;
import java.util.List;

public class TestFilterList {

    public static void main(String[] args) {

        List<Integer> listTest1 = new ArrayList<Integer>();
        List<Integer> listTest2 = new ArrayList<Integer>();

        List<Integer> list = new ArrayList<>();
        list.add(1);
        list.add(2);
        list.add(3);
        list.stream().filter(o -> {
            if (o == 1){
                listTest1.add(o);
                return false;
            }else {
                listTest2.add(o);
                return true;
            }
        }).forEach(o ->{
            System.out.println("..."+o.toString());
        });
        System.out.println(list.toString());
        System.out.println(listTest1.toString());
        System.out.println(listTest2.toString());
    }
}
